using AutoMapper;
using Proje.Common.DTOs.PaymentType;
using Proje.Common.Extensions;
using Proje.Web.UI.Areas.Admin.Models.PaymentTypeViewModels;
using System;
using System.Collections.Generic;
using System.Linq;
using System.Threading.Tasks;

namespace Proje.Web.UI.Infrasructure.Mappers
{
    public class PaymentTypeMapperProfile : Profile
    {
        public PaymentTypeMapperProfile()
        {
            CreateMap<PaymentTypeViewModel, PaymentTypeRequest>()
               .ReverseMap()
               .IgnoreAllNonExisting()
               .ForAllMembers(option => option.Condition((src, dest, srcMember) => srcMember != null));

            CreateMap<PaymentTypeViewModel, PaymentTypeResponse>()
                .ReverseMap()
                .IgnoreAllNonExisting()
                .ForAllMembers(option => option.Condition((src, dest, srcMember) => srcMember != null));

            CreateMap<CreatePaymentTypeViewModel, PaymentTypeRequest>()
                .ReverseMap()
                .IgnoreAllNonExisting()
                .ForAllMembers(option => option.Condition((src, dest, srcMember) => srcMember != null));

            CreateMap<CreatePaymentTypeViewModel, PaymentTypeResponse>()
                .ReverseMap()
                .IgnoreAllNonExisting()
                .ForAllMembers(option => option.Condition((src, dest, srcMember) => srcMember != null));

            CreateMap<UpdatePaymentTypeViewModel, PaymentTypeRequest>()
                .ReverseMap()
                .IgnoreAllNonExisting()
                .ForAllMembers(option => option.Condition((src, dest, srcMember) => srcMember != null));

            CreateMap<UpdatePaymentTypeViewModel, PaymentTypeResponse>()
                .ReverseMap()
                .IgnoreAllNonExisting()
                .ForAllMembers(option => option.Condition((src, dest, srcMember) => srcMember != null));
        }
    }
}
