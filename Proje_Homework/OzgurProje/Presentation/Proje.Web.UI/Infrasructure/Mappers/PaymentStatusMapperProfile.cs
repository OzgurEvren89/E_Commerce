using AutoMapper;
using Proje.Common.DTOs.PaymentStatus;
using Proje.Common.Extensions;
using Proje.Web.UI.Areas.Admin.Models.PaymentStatusViewModels;
using System;
using System.Collections.Generic;
using System.Linq;
using System.Threading.Tasks;

namespace Proje.Web.UI.Infrasructure.Mappers
{
    public class PaymentStatusMapperProfile : Profile
    {
        public PaymentStatusMapperProfile()
        {
            CreateMap<PaymentStatusViewModel, PaymentStatusRequest>()
               .ReverseMap()
               .IgnoreAllNonExisting()
               .ForAllMembers(option => option.Condition((src, dest, srcMember) => srcMember != null));

            CreateMap<PaymentStatusViewModel, PaymentStatusResponse>()
                .ReverseMap()
                .IgnoreAllNonExisting()
                .ForAllMembers(option => option.Condition((src, dest, srcMember) => srcMember != null));

            CreateMap<CreatePaymentStatusViewModel, PaymentStatusRequest>()
                .ReverseMap()
                .IgnoreAllNonExisting()
                .ForAllMembers(option => option.Condition((src, dest, srcMember) => srcMember != null));

            CreateMap<CreatePaymentStatusViewModel, PaymentStatusResponse>()
                .ReverseMap()
                .IgnoreAllNonExisting()
                .ForAllMembers(option => option.Condition((src, dest, srcMember) => srcMember != null));

            CreateMap<UpdatePaymentStatusViewModel, PaymentStatusRequest>()
                .ReverseMap()
                .IgnoreAllNonExisting()
                .ForAllMembers(option => option.Condition((src, dest, srcMember) => srcMember != null));

            CreateMap<UpdatePaymentStatusViewModel, PaymentStatusResponse>()
                .ReverseMap()
                .IgnoreAllNonExisting()
                .ForAllMembers(option => option.Condition((src, dest, srcMember) => srcMember != null));
        }
    }
}
