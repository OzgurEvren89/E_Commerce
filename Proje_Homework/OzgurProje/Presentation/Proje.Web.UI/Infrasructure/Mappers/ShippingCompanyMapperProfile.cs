using AutoMapper;
using Proje.Common.DTOs.ShippingCompany;
using Proje.Common.Extensions;
using Proje.Web.UI.Areas.Admin.Models.ShippingCompanyViewModels;
using System;
using System.Collections.Generic;
using System.Linq;
using System.Threading.Tasks;

namespace Proje.Web.UI.Infrasructure.Mappers
{
    public class ShippingCompanyMapperProfile : Profile
    {
        public ShippingCompanyMapperProfile()
        {
            CreateMap<ShippingCompanyViewModel, ShippingCompanyRequest>()
                .ReverseMap()
                .IgnoreAllNonExisting()
                .ForAllMembers(option => option.Condition((src, dest, srcMember) => srcMember != null));

            CreateMap<ShippingCompanyViewModel, ShippingCompanyResponse>()
                .ReverseMap()
                .IgnoreAllNonExisting()
                .ForAllMembers(option => option.Condition((src, dest, srcMember) => srcMember != null));

            CreateMap<CreateShippingCompanyViewModel, ShippingCompanyRequest>()
                .ReverseMap()
                .IgnoreAllNonExisting()
                .ForAllMembers(option => option.Condition((src, dest, srcMember) => srcMember != null));

            CreateMap<CreateShippingCompanyViewModel, ShippingCompanyResponse>()
                .ReverseMap()
                .IgnoreAllNonExisting()
                .ForAllMembers(option => option.Condition((src, dest, srcMember) => srcMember != null));

            CreateMap<UpdateShippingCompanyViewModel, ShippingCompanyRequest>()
                .ReverseMap()
                .IgnoreAllNonExisting()
                .ForAllMembers(option => option.Condition((src, dest, srcMember) => srcMember != null));

            CreateMap<UpdateShippingCompanyViewModel, ShippingCompanyResponse>()
                .ReverseMap()
                .IgnoreAllNonExisting()
                .ForAllMembers(option => option.Condition((src, dest, srcMember) => srcMember != null));
        }
    }
}
